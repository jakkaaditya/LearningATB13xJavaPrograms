package ex_04_Operators;

public class Relations_operators {

    public static void main (String[] ar)
    {
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal

        // All of them will result boolean output.

        int a = 20;
        int b = 30;

        System.out.println(a>b);
        System.out.println(a!=b);



    }
}

/*output:-
        false
        true*/
