package ex_04_Operators;

public class Operators_Arithmetic_operators {

    public static void main (String[] args)
    {  int age = 30;
        int age2 = 65;

        // int -> data type
        // age -> identifier
        // = -> Assignment operator
        // 65 -> Literal
        System.out.println(age +age2 );
        System.out.println(age-age2);
        System.out.println(age*age2);
        System.out.println(age/age2);

    }
}
