package ex_06_Ternary_Operator;

public class Ternary_Oper {
    public static void main(String[] args) {
        // Age to vote

        int  age =10;
  // This is the systax of the ternary operator, if condition pass next value will trigger if not other value
        String vote = age == 18 ? "yes" : "no";
        System.out.println(vote);


    }
}

