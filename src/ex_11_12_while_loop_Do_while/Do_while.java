package ex_11_12_while_loop_Do_while;

public class Do_while {

    public static void main(String[] args) {
        int a = 1;
        do {
            System.out.println("This is a Body, Which will execute 1 time");
            System.out.println(a);
            a++;

        } while (a < 10);


    }


    }

